    private void ReservebtnPressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ReservebtnPressed
        
        try{
            //get dates
            checkInDate = CheckInDate.getDate();
            checkOutDate = CheckOutDate.getDate();
        
            //Call Booking Manager Class
            reserve = new BookingManager(checkInDate, checkOutDate);
            
            //Check both date validation
            result = reserve.Verify();

            if (result) {
                setUnavailableRoomToDisable();
                BookingPanel.setVisible(false);
                SelectRoomPanel.setVisible(true);
            }
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Please select a date!");
        }
        
    }//GEN-LAST:event_ReservebtnPressed

   
