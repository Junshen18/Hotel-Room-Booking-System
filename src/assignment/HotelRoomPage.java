    @SuppressWarnings("empty-statement")
    private void FormConfirmbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FormConfirmbtnActionPerformed
        
        checkInDate = CheckInDate.getDate();
        checkOutDate = CheckOutDate.getDate();
        roomresult = RoomIDForm.getText();
        intDoS = Integer.parseInt((DoSForm.getText()).replaceAll("[^0-9]", ""));
        customerName = CustomerNameForm.getText();
        icNumber = ICForm.getText();
        contactNumber = ContactForm.getText();
        customerEmail = EmailForm.getText();
        paymentMethod = PaymentMethodtext.getSelectedItem().toString();
        CustomerValidator validator = new CustomerValidator();
        pm = new PaymentManager( intDoS, Integer.parseInt(roomresult));
        
        //boolean for validation
        checkEmail = validator.validateCustomerEmail(customerEmail);
        checkName = validator.validateCustomerName(customerName);
        checkIC = validator.validateCustomerICNumber(icNumber);
        checkContact = validator.validateCustomerContact(contactNumber);

        // check if any of the input are empty
        if (customerName.isEmpty() || icNumber.isEmpty() || contactNumber.isEmpty() || customerEmail.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill up all the details");
            
        } else {
            
            //Check for validations
            if (!checkName){
                JOptionPane.showMessageDialog(null, "Please enter a valid name!");
            } else if (!checkContact){
                JOptionPane.showMessageDialog(null, "Please enter a valid contact number!");
            } else if (!checkIC){
                JOptionPane.showMessageDialog(null, "Please enter a IC number!");
            } else if (!checkEmail){
                JOptionPane.showMessageDialog(null, "Please enter a valid email!");
            } else {
                // all JTextFields are filled, convert the input and create a new booking

                //use BookingManage class to compile all the data
                BookingManager bm = new BookingManager(checkInDate, checkOutDate, roomresult, intDoS, customerName, icNumber, contactNumber, customerEmail, paymentMethod);
                bm.setCustomerName(customerName);
                bm.setCustomerEmail(customerEmail);
                bm.setICNumber(icNumber);
                bm.setContactNumber(contactNumber);

                bookingDetailsDisplay = (
                        "Check-In Date: "+bm.getCheckInDateinStr()+"\n"+
                        "Check-Out Date: "+bm.getCheckOutDateinStr()+"\n"+
                        "Room ID: "+bm.getRoomID()+"\n"+
                        "Day of Stay: "+bm.getDoS()+"\n"+
                        "Customer Name: "+bm.getCustomerName()+"\n"+
                        "Customer Email: "+bm.getCustomerEmail()+"\n"+
                        "IC/Passport Number: "+bm.getICNumber()+"\n"+
                        "Contact Number: "+bm.getContactNumber()+"\n"+
                        "Payment Method: "+bm.getPaymentMethod()+"\n"+
                        "Total Payment: RM "+(String.format("%.2f",pm.getTotalPayment())));

                CIDinStr = bm.getCheckInDateinStr();
                CODinStr = bm.getCheckOutDateinStr();
                totalPayment = pm.getTotalPayment();

                bookingDetails = (customerName+", "+contactNumber+", "+customerEmail+", "+icNumber+", "+roomresult+", "+CIDinStr+", "+CODinStr+", "+intDoS+", "+totalPayment);

                //ask user to confirm the customer's details
                option = JOptionPane.showConfirmDialog(null, bookingDetailsDisplay, "Confirm Detail", JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.OK_OPTION){
                    fm.AddToFile(bookingDetails, bookingFile);
                    //back to Booking Panel
                    NavigateToBookingPanel();
                    ClearSelectionData();
                }
            }
            
        }
    }//GEN-LAST:event_FormConfirmbtnActionPerformed
    
