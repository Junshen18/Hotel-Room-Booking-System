    private void BookRoombtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookRoombtnActionPerformed
        
        //get the selected radio button
        JRadioButton selectedButton = null;
        Enumeration<AbstractButton> roombtns = RoomSelection.getElements();
        //loop inside button group and pick up the first element return true 
        while (roombtns.hasMoreElements()){
            AbstractButton roombtn = roombtns.nextElement();
            if (roombtn.isSelected()){
                selectedButton = (JRadioButton) roombtn;
                break;
            }
        }
        

        DoS = DateManager.countDays(checkInDate, checkOutDate);
        //if the radiobutton grp is not null, fetch the selected button's value to the next panel and show next panel
        if (selectedButton != null){
            roomresult = selectedButton.getText();
            CIDForm.setDate(checkInDate);
            CODForm.setDate(checkOutDate);
            RoomIDForm.setText(roomresult);
            DoSForm.setText(DoS+" Day/s");
            pm = new PaymentManager((int) DoS,Integer.parseInt(roomresult));
            
            subtotalValue.setText(String.format("%.2f",pm.getPaymentb4Tax()));
            serviceTaxValue.setText(String.format("%.2f",pm.getServiceTax()));
            tourismTaxValue.setText(String.format("%.2f",pm.getTourismTax()));
            totalValue.setText("RM "+String.format("%.2f",pm.getTotalPayment()));
            
            SelectRoomPanel.setVisible(false);
            BookingFormPanel.setVisible(true);
        } else {
            //ask user select one room
            JOptionPane.showMessageDialog(null, "Please select a room!");
        }
        
    }//GEN-LAST:event_BookRoombtnActionPerformed
    
